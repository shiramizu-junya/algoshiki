# 自分の回答
#########################
n = gets.to_i
count = 0
(1..n).each do |i|
  count += 1 if n % i == 0
end
puts count

#########################
# リファクタリング
#########################
n = gets.to_i
puts (1..n).select { |i| n % i == 0 }.size

#########################
# 別解_1
#########################
N = gets.to_i
puts (1..N).count{|i| N % i == 0}
